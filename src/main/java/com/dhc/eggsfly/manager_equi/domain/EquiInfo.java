package com.dhc.eggsfly.manager_equi.domain;

public class EquiInfo {
	private String equipment_name;
	private String room_id;
	private String equipment_amount;
	private String equipment_id;

	public String getEquipment_name() {
		return equipment_name;
	}

	public void setEquipment_name(String equipment_name) {
		this.equipment_name = equipment_name;
	}

	public String getRoom_id() {
		return room_id;
	}

	public void setRoom_id(String room_id) {
		this.room_id = room_id;
	}

	public String getEquipment_amount() {
		return equipment_amount;
	}

	public void setEquipment_amount(String equipment_amount) {
		this.equipment_amount = equipment_amount;
	}

	public String getEquipment_id() {
		return equipment_id;
	}

	public void setEquipment_id(String equipment_id) {
		this.equipment_id = equipment_id;
	}

}
